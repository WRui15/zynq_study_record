module ram_16_256 (
    input  wire        clka,
    input  wire        ena,
    input  wire        wea,
    input  wire [7:0]  addra,
    input  wire [15:0] dina,
    input  wire        clkb,
    input  wire        enb,
    input  wire [7:0]  addrb,
    output reg  [15:0] doutb
);
    reg [15:0] mem [0:255];

    always @(posedge clka) begin
        if (ena && wea)
            mem[addra] <= dina;
    end

    always @(posedge clkb) begin
        if (enb)
            doutb <= mem[addrb];
    end
endmodule

module fifo_ram_to_uart (
    input  wire        rst,
    input  wire        wr_clk,
    input  wire        rd_clk,
    input  wire [15:0] din,
    input  wire        wr_en,
    input  wire        rd_en,
    output reg  [7:0]  dout,
    output wire        full,
    output wire        almost_full,
    output wire        empty,
    output wire        almost_empty,
    output wire [9:0]  rd_data_count,
    output wire [8:0]  wr_data_count,
    output wire        wr_rst_busy,
    output wire        rd_rst_busy
);
    reg [7:0] mem [0:511];
    reg [8:0] wr_ptr;
    reg [8:0] rd_ptr;
    reg [9:0] count;

    assign empty         = (count == 0);
    assign almost_empty  = (count <= 1);
    assign full          = (count >= 512);
    assign almost_full   = (count >= 510);
    assign rd_data_count = count;
    assign wr_data_count = count[8:0];
    assign wr_rst_busy   = 1'b0;
    assign rd_rst_busy   = 1'b0;

    always @(posedge wr_clk or posedge rst) begin
        if (rst) begin
            wr_ptr <= 9'd0;
        end else if (wr_en && !full) begin
            mem[wr_ptr]     <= din[7:0];
            mem[wr_ptr + 1] <= din[15:8];
            wr_ptr          <= wr_ptr + 2'd2;
        end
    end

    always @(posedge rd_clk or posedge rst) begin
        if (rst) begin
            rd_ptr <= 9'd0;
            dout   <= 8'd0;
            count  <= 10'd0;
        end else begin
            if (wr_en && !full && !(rd_en && !empty))
                count <= count + 2'd2;
            else if (rd_en && !empty && !(wr_en && !full))
                count <= count - 1'b1;

            if (rd_en && !empty) begin
                dout   <= mem[rd_ptr];
                rd_ptr <= rd_ptr + 1'b1;
            end
        end
    end
endmodule
